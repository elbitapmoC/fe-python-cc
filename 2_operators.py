# Addition (+)
# If there's a float involved, the answer will be a float.
print(5.0 + 2)  # 7.0

# Subtraction (-)
print(1 - 4)

# Division
print(4 / 2)

# Multiplication
print(3 * 7)

# Exponents
print(3**2)  # 3*3 = 9

# Integer Division - Rounds down
print(10//3)  # 10/3 = 3

# Modulo - Remainder
print(10 % 3)
